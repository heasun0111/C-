#include <stdio.h>


int main()

{

	int num, i;

	int arrA[i];

	int arrB[i];



	scanf("%d", &num);



	for (i = 0; i < num; i++) {

		scanf("%d", &arrA[i]);

		scanf("%d", &arrB[i]);



	}

	for (i = 0; i < num; i++) {

		printf("Case #%d: %d + %d= %d\n", i + 1, arrA[i], arrB[i], arrA[i] + arrB[i]);



	}




}
